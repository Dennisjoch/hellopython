# 3 Ziel: Zwei Zahlen vom Benutzer erfragen und ihre Summe ausgeben.

# 1. Frage zwei Zahlen mit der input-Funktion ab.
# 2. Wandele die Eingaben in Ganzzahlen (int) um.
# 3. Addiere die Zahlen und gib das Ergebnis mit print aus.

zahl1 = int(input("Gib die erste Zahl ein: "))
zahl2 = int(input("Gib die zweite Zahl ein: "))
ergebnis = zahl1 + zahl2
print("Die Summe von", zahl1, "und", zahl2, "ist", ergebnis)  # Hier kommt dein Code hin!
